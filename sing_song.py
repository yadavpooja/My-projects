class Song():
    def __init__(self,lyrics):
        self.lyrics = lyrics


    def sing(self):
        return self.lyrics






Birthday = Song("Happy B'day To you\nMay god bless u,\nHappy b'day to you....")
print(Birthday.sing())


Star_poem = Song("\nTwinkle Twinkle little star\nHow I wonder what you are\nUp above the world so high,\nlike a diamond in the sky")
print(Star_poem.sing())


